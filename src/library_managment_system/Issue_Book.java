    private void btnissueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnissueActionPerformed
        if (txtbookid.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Please enter Book ID and search it");
        txtbookid.requestFocus();  
        }
         else{
            try{
            pst = con.prepareStatement("UPDATE book SET status='Issued', issuedate=?, duedate=?, studentid=? WHERE id=?");
            pst.setString(1, txtissue.getText());
            pst.setString(2, txtduedate.getText());
            pst.setString(3, txtstudentid.getText());
            pst.setString(4, txtbookid.getText()); 
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Book Issued");
            txtbookid.setText("");
            txtduedate.setText("");
            txtbook.setText("");
            txtstudentid.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(Issue_Book.class.getName()).log(Level.SEVERE, null, ex);}
        }   
        
    }//GEN-LAST:event_btnissueActionPerformed

