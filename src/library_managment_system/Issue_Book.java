    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
         try {
        pst = con.prepareStatement("SELECT * FROM library.book WHERE id=?");
        pst.setString(1, txtbookid.getText()); // Correct field used now
        rs = pst.executeQuery();
        if (rs.next()) {
            txtbook.setText(rs.getString("name"));
        } else {
            JOptionPane.showMessageDialog(this, "Please enter a valid Book ID");
        }
    } catch (SQLException ex) {
        Logger.getLogger(Issue_Book.class.getName()).log(Level.SEVERE, null, ex);
    }      
    }//GEN-LAST:event_btnsearchActionPerformed

